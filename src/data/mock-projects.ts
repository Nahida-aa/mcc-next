// 根据参数筛选项目的函数
export function filterProjects(params: ListProjectParams): ProjectMeta[] {
  let filtered = mockProjects.filter((project) => project.type === params.type)

  // 关键字搜索
  if (params.keyword) {
    const keyword = params.keyword.toLowerCase()
    filtered = filtered.filter(
      (project) =>
        project.name.toLowerCase().includes(keyword) ||
        project.summary.toLowerCase().includes(keyword) ||
        project.tags.some((tag) => tag.toLowerCase().includes(keyword)),
    )
  }

  // 游戏版本筛选
  // TypeScript 对于 if (params.loaders && params.loaders.length > 0) 这种类型收窄，在简单赋值时能推断出来，但直接在表达式里用（比如 params.loaders.some(...)），有时推断不够智能。
  // 这属于类型收窄的“局部失效”问题。
  if (params.gameVersions && params.gameVersions.length > 0) {
    const gameVersions = params.gameVersions
    filtered = filtered.filter((project) =>
      gameVersions.some((version) => project.gameVersions.includes(version)),
    )
  }

  // 加载器筛选
  if (params.loaders && params.loaders.length > 0) {
    const loaders = params.loaders
    filtered = filtered.filter((project) => loaders.some((loader) => project.loaders.includes(loader)))
  }

  // 环境筛选
  if (params.environment) {
    filtered = filtered.filter(
      (project) => project.environment === params.environment || project.environment === "both",
    )
  }

  // 开源筛选
  if (params.isOpenSource !== undefined) {
    filtered = filtered.filter((project) => project.isOpenSource === params.isOpenSource)
  }

  // 标签筛选
  if (params.tags && params.tags.length > 0) {
    const tags = params.tags
    filtered = filtered.filter((project) => tags.some((tag) => project.tags.includes(tag)))
  }

  // 排序
  const sortField = params.sort || "relevance"
  const sortDirection = params.sortDirection || "desc"

  filtered.sort((a, b) => {
    let aValue: any, bValue: any

    switch (sortField) {
      case "downloadCount":
        aValue = a.downloadCount
        bValue = b.downloadCount
        break
      case "followCount":
        aValue = a.followCount
        bValue = b.followCount
        break
      case "published_at":
        aValue = new Date(a.createdAt)
        bValue = new Date(b.createdAt)
        break
      case "updated_at":
        aValue = new Date(a.updatedAt)
        bValue = new Date(b.updatedAt)
        break
      default: // relevance
        aValue = a.downloadCount + a.followCount
        bValue = b.downloadCount + b.followCount
    }

    if (sortDirection === "asc") {
      return aValue > bValue ? 1 : -1
    } else {
      return aValue < bValue ? 1 : -1
    }
  })

  // 分页
  const offset = params.offset || 0
  const limit = params.limit || 20

  return filtered.slice(offset, offset + limit)
}